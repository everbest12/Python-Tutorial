Age = int(input("Enter your age in years: "))

Year_to_Month = 12
year_to_weeks =  52
year_to_days = 365

Year_in_Month = Year_to_Month * Age
year_in_weeks =  year_to_weeks * Age
year_in_days =  year_to_days * Age


print (f" You've lived approximately: \n {Year_in_Month} Months \n {year_in_weeks} Weeks \n {year_in_days} Days")